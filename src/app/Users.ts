export class Users
{
    id : any;
    name : any;
    email : any;
    gender : any;
    active : any;
    mobile : any;

    constructor(id: any, name: any, email: any, gender: any, active: any, mobile: any)
    {
        this.id = id;
        this.name = name;
        this.email = email;
        this.gender = gender ;
        this.active = active;
        this.mobile = mobile;
    }

}